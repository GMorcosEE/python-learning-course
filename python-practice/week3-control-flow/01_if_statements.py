# Week 3: If Statements - Making Decisions
# Run: python3 week3-control-flow/01_if_statements.py

# Basic if statement
age = 18

if age >= 18:
    print("You are an adult")

# If-else statement
temperature = 75

if temperature > 80:
    print("It's hot outside!")
else:
    print("The weather is nice")

# If-elif-else statement
score = 85

if score >= 90:
    print("Grade: A")
elif score >= 80:
    print("Grade: B")
elif score >= 70:
    print("Grade: C")
else:
    print("Grade: F")

