# Week 2: More Number Operations
# Run: python3 week2-basics/02_number_practice.py

# Different types of numbers
whole_number = 42
decimal_number = 3.14
negative_number = -10

print("Whole:", whole_number)
print("Decimal:", decimal_number)
print("Negative:", negative_number)

# More math operations
print("Power:", 2 ** 3)  # 2 to the power of 3 = 8
print("Remainder:", 10 % 3)  # 10 divided by 3, remainder = 1

# Converting between types
age_text = "25"
age_number = int(age_text)  # Convert text to number
print("Age + 5:", age_number + 5)

price = 19.99
print("Price as integer:", int(price))  # Removes decimal

