# Get user information
name = input("What's your name? ")
age = input("How old are you? ")
city = input("What city do you live in? ")
hobby = input("What's your favorite hobby? ")

# Create a formatted card
print("\n" + "=" * 40)
print("PERSONAL INFO CARD")
print("=" * 40)
print("Name:  ", name)
print("Age:   ", age)
print("City:  ", city)
print("Hobby: ", hobby)
print("=" * 40)

