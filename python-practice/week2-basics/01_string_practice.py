# Week 2: Working with Strings (Text)
# Run: python3 week2-basics/01_string_practice.py

# String operations
first_name = "John"
last_name = "Doe"

# Combining strings
full_name = first_name + " " + last_name
print("Full name:", full_name)

# String length
print("Name length:", len(full_name))

# Uppercase and lowercase
print("Uppercase:", full_name.upper())
print("Lowercase:", full_name.lower())

# Repeating strings
print("Ha" * 5)  # Prints "HaHaHaHaHa"

